
#include "Vector3.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
inline Vector3& Vector3::operator+=(const Vector3& v)
{
	x += v.x;
	y += v.y;
	z += v.z;
	
	return *this;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
inline Vector3& Vector3::operator-=(const Vector3& v)
{
	x -= v.x;
	y -= v.y;
	z -= v.z;

	return *this;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
inline Vector3& Vector3::operator*=(const Vector3& v)
{
	x *= v.x;
	y *= v.y;
	z *= v.z;

	return *this;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
inline Vector3& Vector3::operator/=(const Vector3& v)
{
	x /= v.x;
	y /= v.y;
	z /= v.z;

	return *this;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
inline Vector3& Vector3::operator*=(const float v)
{
	x *= v;
	y *= v;
	z *= v;

	return *this;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
inline Vector3& Vector3::operator/=(const float v)
{
	x /= v;
	y /= v;
	z /= v;

	return *this;
}
